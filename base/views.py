@api_view(['GET'])
def getProducts(request):
    query = request.query_params.get('keyword')
    query2 = request.query_params.get('category')
    if query == None:
        query = ''
    if query2 == None or query2 == 'all':
        query2 = ''
    products = Product.objects.filter(Q(name__icontains=query,category__icontains=query2) | Q(name_ar__icontains=query,category__icontains=query2)).order_by('-createdAt')

    page = request.query_params.get('page')
    paginator = Paginator(products, 8)

    try:
        products = paginator.page(page)
    except PageNotAnInteger:
        products = paginator.page(1)
    except EmptyPage:
        products = paginator.page(paginator.num_pages)

    if page == None:
        page = 1

    page = int(page)
    serializer = ProductSerializer(products,many=True,context={'request': request})
    return Response({'products': serializer.data, 'page': page, 'pages': paginator.num_pages})
